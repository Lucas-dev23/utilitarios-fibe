export function buscarGrade(id) {
    return gradesCurriculares.find(g => g.id === id);
}

export function buscarPeriodo(gradeId, periodoId) {

    const grade = buscarGrade(gradeId);

    if (!grade) {
        return 0;
    }

    return grade.periodos.find(p => p.id === periodoId);
}

export function calcularCHTotalPeriodo(gradeId, periodoId) {

    const periodo = buscarPeriodo(gradeId, periodoId);

    if (!periodo) {
        return 0;
    }

    return periodo.disciplinas.reduce(
        (total, disciplina) => total + disciplina.carga, 0);
}